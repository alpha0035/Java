package working_with_variables_datatypes;
// primitive data types is defined by the language itself

public class primitiveTypes {
    // in Java, primitive types include:
    byte x;     // 8-bit integer
    short y;    //16-bit integer
    int z;      //32-bit integer
    long t;     //64-bit integer
    float u;    //32-bit floating-point 
    double v;   //64-bit floating-point
    char a;     //16-bit character Unicode scheme
    boolean f;  //true or false
}
